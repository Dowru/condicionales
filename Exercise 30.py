
numDay = int(input("Enter a number (1-7): "))

if numDay == 1:
    print("Sunday")
elif numDay == 2:
    print("Monday")
elif numDay == 3:
    print("Tuesday")
elif numDay == 4:
    print("Wednesday")
elif numDay == 5:
    print("Thursday")
elif numDay == 6:
    print("Friday")
elif numDay == 7:
    print("Saturday")
else:
    print("Invalid number. Please enter a number between 1 and 7.")
